#!/usr/bin/env python3
"""
Triforce ASCII Art

This script prints a Triforce symbol using ASCII characters.
The Triforce consists of three triangles arranged in a triangular pattern.
"""

def main():
    """Print the Triforce ASCII art pattern."""
    print("      /\\")
    print("     /  \\")
    print("    /____\\")
    print("   /\\    /\\")
    print("  /  \\  /  \\")
    print(" /____\\/____\\")

if __name__ == "__main__":
    main()
